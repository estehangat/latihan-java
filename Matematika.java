public class Matematika {
    public static void main(String[] args) {
        
        int a = 21;
        int b = 9;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        System.out.print("\n");
        
        int c = 51;
        
        c += 65;
        System.out.println(c);
        
        c -= 65;
        System.out.println(c);
        
        c *= 65;
        System.out.println(c);
        
        c /= 65;
        System.out.println(c);
        
        c %= 65;
        System.out.println(c);
        
        System.out.print("\n");
        
        int d = 100;
        d++;

        int e = 100;
        e--;

        System.out.println(d);
        System.out.println(e);
    }    
}
